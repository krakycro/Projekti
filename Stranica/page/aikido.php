<p>Aikido (合気道 aikidō, također 合氣道 koristeći stariji stil <a href="http://hr.wikipedia.org/wiki/Kanji">kanji</a> pisma), doslovno znači "harmonija energija put" što bi se prevelo kao "način harmonije duha" je gendai budo — moderna Japanska borilačka vještina. Ljudi koji treniraju aikido su obično poznati kao aikidoke. Aikido je osmislio Morihei Uešiba (植芝盛平) (također poznat među aikidokama kao o-sensei (翁先生) tijekom razdoblja od 1930e do 1960e. Tehnički, veliki dio aikida je baziran na daitō-rjū aiki-jūđucu (大東流合気柔術), tipu jujutsu i kenjutsua 　(剣術) odnosno Japanske tehnike rukovanja mačem (također neki vjeruju da je vještina yađū šinkage rjū utjecala na razvoj taktike u aikidu). Aikido posjeduje i jednu značajnu duhovnu komponentu.</p>
<h4>Povijest</h4>
<hr style="width:100%">
<p>Ime aikido nastalo je od 3 japanska znaka, 合気道 : ai, ki i do. Značenje navedenih znakova obično se prevodi kao harmonija, energija i put tako da aikido može biti preveden kao "način harmonije kroz energiju". Druga uobičajena interpretacija znakova je harmonija, duh i put što bi se moglo prevesti kao "način duhovne harmonije". Oba prijevoda govore o činjenici da su aikido tehnike razvijene tako da kontroliraju napadača kontrolirajući i preusmjeravajući napadačevu energiju umjesto da se blokira. (Korejska borilačka vještina poznata kao hapkido koristi ista tri znaka: neki misle kako je to povijesna poveznica kroz daito rju, glavni izvor aikido tehnika).
Morihei Ueshiba razvio je aikido ponajviše bazirajući se na Daito Ryu aikijutsu uključivajući pokrete, te koristeći jari (koplje), đo (kratki štap), i vjerojatno juken (bajunet). Ali definitivno najveći utjecaj je korištenje katane (mač). U mnogo slučajeva, aikidoka (osoba koja trenira aikido) kreće se poput golorukog mačevaoca. Aikido udarci šomenući i jokomenući izvorno dolaze od napada oružjem, i rezultati tehnika su slični tehnikama oduzimanja oružja. Neke škole aikida ne treniraju uopće s oružjem, dok druge škole poput Iwama Ryu obično potroše mnogo vremena sa bokkenom (drveni mač), đo, i tanto (nož). U većini stilova, sve tehnike se mogu obaviti s mačem, gotovo isto kao i goloruk.
Minoru Moćizuki je donio aikido na Zapad 1951. u Francusku. U Veliku Britaniju aikido je došao 1955., a u SAD 1960-e, u Australiju 1965., kao i u mnoge druge zemlje. Danas postoji mnogo aikido dojoa za treniranje diljem svijeta.</p>
